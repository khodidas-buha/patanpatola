                History </h1>
            <p class="mt-4 mb-4 text-center">
                One of the folk-song of Gujarat in which the wife asks her husband to bring for her patola of patan
                while returning, “Chelaji Re, Mare Hatu patan thi patola mongha lavjo”,(O my dear! Do bring the precious
                patola from patan for me!) has remained very famous for the last so many years, and so is the fame,
                charm and popularity of patola even today.
            </p>
            <p class="mt-4 mb-4 text-center">
                Patola is a holy silk-cloth. It’s believed that by wearing patola containing religious and miraculous
                powers, one feels God. It was this quality, which made the people of Thailand, Malaysiya and Indonesia
                Value the Patola above all treasures. For them it was a magic cloth, which protected you in battle,
                which protected you from evil and bad health. It was the power cloth, which was inherited by the women
                and gave them a very high status.
            </p>
            <p class="mt-4 mb-4 text-center">
                Today, it is gifted to the daughter and daughter-in-law on occasions of marriages in rich families as a
                sign of good omen. It is considered to be wearing a high status. To process and wear patola on holy
                occasions is considered to be a matter of pride of cloth of ma’a or mawa,(Indonesian words meaning
                “created by God”) which was considered so perfect that it could only have been created by the gods.
                Patola has a very old history. It is believed that the word “patola” must have been derived from the
                word “pat”, and in Sanskrit it is called “patta” and in telugu “patola”. It has been mentioned in the
                tour-depiction that in 1342 A.D. a merchant named IBEN BATUTA gifted patolas to many kings in their
                courts.
            </p>
            <p class="mt-4 mb-4 text-center">
                Patolas were amply used in the 17th and 18th centuries. They were used as precious gifts. Getting made
                patolas was as dignified as getting rich ornaments prepared. A French traveler has noted in his dairy
                that the patolas of patan weresent up to Malaysia and Indonesia. Patolas were famous from java, Sumatra
                to Samarkand, basra, damask and rome in the 15 century. Gujarat poet premananad of the 17th century, in
                his famous narrative “KUNVARBAINU MAMERU” has referred to patolas as “Lakho Pachhedi Pandar kodi, Patola
                pachas vahuji”[Kadvu-6][Please note that I will have 300 upper clothes and fifty patolas for marriages]
                “Laish patolu shrikar, sadi nai paheru”[Kadvu-12][I will have the patola of shrikar (sacred shapped),
                but will not wear a saree]. Patolas are also referred in the Duhas of Ranakdevi as : “Juvo Patolawalio
                lombdiwalino kand”. The psychological motif behind the specific combination of color and design of
                Patola is worth understanding. The square arouses the feeling of security, as woman generally desire
                security in every occasions of the life. The Symbols of Elephant, Parrot, Peacock, Kalas(Jug) and the
                Human left are all considered to be the auspicious symbol of saubhagya (A Women’s good luck of having
                husband alive!) The Feeling of good luck and security are imbibed in the Patola, and hence, such
                auspicious symbols are found in them.
